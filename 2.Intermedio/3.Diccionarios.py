"""
Las diccionarios son una estructura de datos que almacena pares clave-valor.
Las claves pueden ser de cualquier tipo de datos, como enteros, flotantes, cadenas de caracteres, etc.
Los valores pueden ser de cualquier tipo de datos, como enteros, flotantes, cadenas de caracteres, etc.
Por ejemplo, si queremos almacenar un diccionario de nombres y edades, podemos hacerlo de la siguiente manera:
"""
diccionario={"nombre":"Juan","edad":25}
print(f"El nombre es {diccionario['nombre']} y la edad es {diccionario['edad']}")
